class RoundWinner {

    /**
     * @param {number} position 
     * @param {string} name 
     * @param {string} id 
     */
    constructor(position, name, id) {
        /**
         * @type {string}
         * @description The unique ID of the Player.
         */
        this.id = id;
        /**
         * @type {number}
         * @description The Position of the Player in Game.
         */
        this.position = position;
        /**
         * @type {name}
         * @description The name of the Player.
         */
        this.position = position;
    }

}

module.exports = RoundWinner
